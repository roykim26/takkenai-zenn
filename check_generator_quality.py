def _print_header(title: str) -> None:
    print("=" * 24)
    print(title)
    print("=" * 24)


def _print_sample_result(name: str, text: str) -> None:
    hits = _find_placeholder_hits(text)
    print(f"[{name}]")
    print(f"placeholder_hits={hits}")
    print()


def _print_article_result(name: str, body: str, min_chars: int) -> None:
    result = _check_article_quality(body, min_chars=min_chars)
    print(f"[{name}]")
    print(f"is_thin={result['is_thin']}")
    print(f"placeholder_hits={result['placeholder_hits']}")
    print(f"placeholder_only={result['placeholder_only']}")
    print(f"reasons={result['reasons']}")
    print()


